package che16.dcs.aber.ac.uk.model;

public class City {

	private int x, y, index;

	public City(int x, int y, int index){
		this.x = x;
		this.y = y;
		this.index = index;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getIndex() {
		return index;
	}


}
